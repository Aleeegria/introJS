// operador ternário -> faz comparação
// em uma linha, escrita reduzida
// if_then_else reduzido

const idadeMinima = 18;
const idadeCliente = 19;

if (idadeCliente >= idadeMinima){
    console.log("Cerveja !!!")
} else {
    console.log("Suco !!!")
}

// operador ternario p/ comparações curtas
// ternário -> 3 comparadores = ( >=, ?, :)
console.log(idadeCliente >= idadeMinima ? "Cerveja!" : "Suco!!!");




